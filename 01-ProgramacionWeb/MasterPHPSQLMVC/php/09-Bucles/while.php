<?php
/*BUCLE WHILE
* Estructura de control que itera o repite la ejecución de una serie de instrucciones
* tantas veces como sea necesario, en base a una condición
*
* while(condicion){
*   bloque de instrucciones
*   otra instrucción 
*}
*/

$numero = 0;
while ($numero <= 100){
    echo "<p>$numero</p>";
    $numero++;
}
?>